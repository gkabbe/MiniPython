"""Werte in Variablen speichern:

Bei komplexeren Problemstellungen ist es oft hilfreich, Zwischenergebnisse zu speichern.
Dies lässt sich mit Variablen realisieren.
"""

# Beispiel:

summand_1 = 5  # Variable mit dem Wert 5
summand_2 = 7

summe = summand_1 + summand_2

print(summand_1, "+", summand_2, "=", summe)

# Wichtig: das Gleichheitszeichen hat hier eine andere Funktion als in der Mathematik
# Es weist es einer Variablen einen Wert zu, drückt aber keine Äquivalenz aus
